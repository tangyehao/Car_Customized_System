package cn.com.beans;


public class Inventory {
	private int iId;
	private int iPartsID; 
	private int iNum;
	public int getiId() {
		return iId;
	}
	public void setiId(int iId) {
		this.iId = iId;
	}
	public int getiPartsID() {
		return iPartsID;
	}
	public void setiPartsID(int iPartsID) {
		this.iPartsID = iPartsID;
	}
	public int getiNum() {
		return iNum;
	}
	public void setiNum(int iNum) {
		this.iNum = iNum;
	}
	
}
