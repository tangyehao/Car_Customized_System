package cn.com.beans;

public class Modle {
	private int mID;
	private String mName;
	private String mType;
	private int mPurchasePrice;
	private int mSellPrice;
	private int mCapacity;
	public int getmID() {
		return mID;
	}
	public void setmID(int mID) {
		this.mID = mID;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getmType() {
		return mType;
	}
	public void setmType(String mType) {
		this.mType = mType;
	}
	public int getmPurchasePrice() {
		return mPurchasePrice;
	}
	public void setmPurchasePrice(int mPurchasePrice) {
		this.mPurchasePrice = mPurchasePrice;
	}
	public int getmSellPrice() {
		return mSellPrice;
	}
	public void setmSellPrice(int mSellPrice) {
		this.mSellPrice = mSellPrice;
	}
	public int getmCapacity() {
		return mCapacity;
	}
	public void setmCapacity(int mCapacity) {
		this.mCapacity = mCapacity;
	}
	
}
