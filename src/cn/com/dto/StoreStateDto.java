package cn.com.dto;

public class StoreStateDto {
	private int storeTotalNumber;
	private double storeTotalPrice;
	private double storePurchasePrice;
	private int storeNumber;
	public int getStoreTotalNumber() {
		return storeTotalNumber;
	}
	public void setStoreTotalNumber(int storeTotalNumber) {
		this.storeTotalNumber = storeTotalNumber;
	}
	public double getStoreTotalPrice() {
		return storeTotalPrice;
	}
	public void setStoreTotalPrice(double storeTotalPrice) {
		this.storeTotalPrice = storeTotalPrice;
	}
	public double getStorePurchasePrice() {
		return storePurchasePrice;
	}
	public void setStorePurchasePrice(double storePurchasePrice) {
		this.storePurchasePrice = storePurchasePrice;
	}
	public int getStoreNumber() {
		return storeNumber;
	}
	public void setStoreNumber(int storeNumber) {
		this.storeNumber = storeNumber;
	}
	
}
