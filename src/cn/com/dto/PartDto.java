package cn.com.dto;

public class PartDto {

	
	
	private int partIdDto;
	private String  partNameDto;
	private String  partColorDto;
	private double  partPriceDto;
	private int partNumberDto;
	private double partTotalPriceDto;
	public int getPartIdDto() {
		return partIdDto;
	}
	public void setPartIdDto(int partIdDto) {
		this.partIdDto = partIdDto;
	}
	public String getPartNameDto() {
		return partNameDto;
	}
	public void setPartNameDto(String partNameDto) {
		this.partNameDto = partNameDto;
	}
	public String getPartColorDto() {
		return partColorDto;
	}
	public void setPartColorDto(String partColorDto) {
		this.partColorDto = partColorDto;
	}
	public double getPartPriceDto() {
		return partPriceDto;
	}
	public void setPartPriceDto(double partPriceDto) {
		this.partPriceDto = partPriceDto;
	}
	public int getPartNumberDto() {
		return partNumberDto;
	}
	public void setPartNumberDto(int partNumberDto) {
		this.partNumberDto = partNumberDto;
	}
	public double getPartTotalPriceDto() {
		return partTotalPriceDto;
	}
	public void setPartTotalPriceDto(double partTotalPriceDto) {
		this.partTotalPriceDto = partTotalPriceDto;
	}
	
}
