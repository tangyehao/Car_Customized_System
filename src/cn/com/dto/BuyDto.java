package cn.com.dto;

public class BuyDto {
	private int buyNumber;
	private int buyBillNumber;
	private int buyMoney;
	private int backNumber;
	private int backBillNumber;
	private int backMoney;
	
	
	public int getBuyNumber() {
		return buyNumber;
	}
	public void setBuyNumber(int buyNumber) {
		this.buyNumber = buyNumber;
	}
	public int getBuyBillNumber() {
		return buyBillNumber;
	}
	public void setBuyBillNumber(int buyBillNumber) {
		this.buyBillNumber = buyBillNumber;
	}
	public int getBuyMoney() {
		return buyMoney;
	}
	public void setBuyMoney(int buyMoney) {
		this.buyMoney = buyMoney;
	}
	public int getBackNumber() {
		return backNumber;
	}
	public void setBackNumber(int backNumber) {
		this.backNumber = backNumber;
	}
	public int getBackBillNumber() {
		return backBillNumber;
	}
	public void setBackBillNumber(int backBillNumber) {
		this.backBillNumber = backBillNumber;
	}
	public int getBackMoney() {
		return backMoney;
	}
	public void setBackMoney(int backMoney) {
		this.backMoney = backMoney;
	}
	
	
}
